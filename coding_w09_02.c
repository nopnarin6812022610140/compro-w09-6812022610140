#include <stdio.h>        // เรียกใช้ไลบรารีมาตรฐานสำหรับการแสดงผล input/output

int main() {              // ฟังก์ชันหลักของโปรแกรม
    int sum, p, x, y;     // ประกาศตัวแปรจำนวนเต็ม sum (ผลรวม), p (ผลคูณ), x และ y
    sum = 0;              // กำหนดค่าเริ่มต้นของ sum = 0

    // วนลูปโดยเริ่ม x=1, y=1 และทำงานต่อเมื่อ x*y <= 15
    // ทุกครั้งที่วนลูป x เพิ่มขึ้นทีละ 1 และ y เพิ่มขึ้นทีละ 2
    for (x = 1, y = 1; x * y <= 15; x++, y += 2) {  
        p = x * y;                    // คำนวณ p เป็นผลคูณของ x และ y
        sum = sum + p;                 // สะสมค่า p ลงใน sum
        printf("%d * %d = %d\n", x, y, p);  // แสดงผลการคูณแต่ละรอบ
    }

    printf("summation of x * y = %d\n", sum); // แสดงผลรวมของการคูณทั้งหมด
    return 0;                      // ส่งค่ากลับ 0 เพื่อบอกว่าโปรแกรมจบการทำงานปกติ
}