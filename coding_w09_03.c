#include <stdio.h>
int main() {
    int i = 1;                    // กำหนดค่าเริ่มต้น i = 1

    while (i <= 10) {             // ตรวจสอบเงื่อนไข i <= 10 ก่อนเข้าลูป
        if (i == 6) {             // ถ้า i เท่ากับ 6
            i++;                  // เพิ่มค่า i ก่อน ไม่งั้นจะวนลูปค้าง
            continue;             // ข้าม printf แล้วกลับไปตรวจเงื่อนไขใหม่
        }
        printf("%d ", i);         // แสดงค่าของ i
        i++;                      // เพิ่มค่า i ทีละ 1
    }
    return 0;                     // จบโปรแกรม
}